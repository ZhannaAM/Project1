from src. dictonary_search import count_transactions, search_transactions


def test_count_transactions(dictionaries):
    list_description = ["Перевод организации", "Перевод со счета на счет", "Перевод с карты на карту"]
    result = count_transactions(dictionaries, list_description)
    assert result == {
        "Перевод организации": 2,
        "Перевод со счета на счет": 2,
        "Перевод с карты на карту": 1
    }


